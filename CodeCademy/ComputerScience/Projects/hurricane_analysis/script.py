def damages_to_float(damage_list):
    b = 1000000000
    m = 1000000
    damage_floats = []
    for i in range(0, len(damage_list)):
        if damage_list[i] == 'Damages not recorded':
            damage_floats.append(damage_list[i])
        elif damage_list[i][-1] == 'B':
            billion = float(damage_list[i][:-1]) * b
            damage_floats.append(billion)
        else:
            million = float(damage_list[i][:-1]) * m
            damage_floats.append(million)
    return damage_floats
